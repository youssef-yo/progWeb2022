    public function handle(Request $request, Closure $next)
    {
        Session::start();

        if (Session::has('logged') || session('logged') === true) {
            if (session('admin')) {
                return Redirect::to(route('admin.index'));
            } else if (session('admin') === false) {
                return Redirect::to(route('client.index'));
            }
        }

        return $next($request);
    }
